<?php

namespace App\Entity;

/**
 * Class Movie.
 */
class Movie
{
    protected int $id;
    protected string $text;

    public function getId(): int
    {
        return $this->id;
    }

    public function setId(int $id): self
    {
        $this->id = $id;

        return $this;
    }

    public function getText(): string
    {
        return $this->text;
    }

    public function setText(string $text): self
    {
        $this->text = $text;

        return $this;
    }
}
